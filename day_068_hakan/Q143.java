package day_068_hakan;

public class Q143 {
    char c;
    boolean b;
    float f;
    void printAll() {
        System.out.println("c = " + c); // 0/u000
        System.out.println("b = " + b); // false
        System.out.println("f = " + f); // 0.0
    }

    public static void main(String[] args) {
        Q143 f = new Q143();
        f.printAll();
    }
}
